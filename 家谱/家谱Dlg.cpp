void CMyDlg::OnSave() //可以在保存时，增加新的txt文件。
{
	// TODO: Add your control notification handler code here
	UpdateData(true);//获取编辑框中输入的内容。
	CFileDialog dlg(false,"txt",NULL,OFN_HIDEREADONLY|OFN_OVERWRITEPROMPT,"文本文件(*.txt)|*.txt|所有文件(*.*)|*.*||");
	if(dlg.DoModal()==IDOK){
	    CString m_FilePath=dlg.GetPathName();
		CString m_path=m_FilePath;//得到文件路径
		UpdateData(FALSE);
        
		CFile file;
        file.Open(m_path,CFile::modeCreate | CFile::modeReadWrite); //打开文件

	  CString str1,str2,str3,st3;
	  str1 = "姓名："+m_name+'\n';
	  file.Write(str1,str1.GetLength());//将内容写入到文件中去。
	  str2 = "性别："+m_sex+'\n';
	  file.Write(str2,str2.GetLength());
	  st3.Format("%d",m_birth);//将int型数据转为string型。
	  str3 = "生日："+st3+'\n';
	  file.Write(str3,str3.GetLength());

	  file.Close();
	
	}
}
